// Generate language paths grouped by language first
const generateLanguagePathsGrouped = (basePaths, languages) => {
  const languagePaths = [];

  // Loop through each language first
  for (const lang of languages) {
    // Then loop through all base paths for this language
    for (const basePath of basePaths) {
      languagePaths.push(`${basePath}/${lang}`);
    }
  }

  return languagePaths;
};

// Generate the full array
const allLanguagePaths = generateLanguagePathsGrouped(basePaths, languages);

// Output as JSON - all python first, then all rust, etc.
console.log(JSON.stringify(allLanguagePaths, null, 2));

// For your specific example with just ["/code/garch", "/code/rsi"]
const exampleBasePaths = ["/code/garch", "/code/rsi"];
const exampleLanguagePaths = generateLanguagePathsGrouped(
  exampleBasePaths,
  languages,
);
console.log(
  "\n\n// Example output for /code/garch and /code/rsi (all python first):\n",
);
console.log(JSON.stringify(exampleLanguagePaths, null, 2));

